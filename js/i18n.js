// Default language
let currentLang = 'en';

// Get translation
function t(key) {
  return i18n[currentLang][key] || i18n['en'][key] || key;
}

// Change language
function setLanguage(lang) {
  if (i18n[lang]) {
    currentLang = lang;
    document.documentElement.lang = lang;
    document.documentElement.dir = i18n[lang].dir || 'ltr';
    document.title = t('siteTitle');
    updateAllTexts();
    localStorage.setItem('yishui-lang', lang);
    // Update lang switcher display
    const langBtn = document.getElementById('langCurrent');
    if (langBtn) langBtn.textContent = i18n[lang].langName;
    // Notify content loader of language change
    window.dispatchEvent(new CustomEvent('contentLangChanged', { detail: { lang: lang } }));
  }
}

// Update all texts on page
function updateAllTexts() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = t(key);
    } else {
      el.textContent = t(key);
    }
  });
  // Update direction for RTL
  document.documentElement.dir = i18n[currentLang].dir || 'ltr';
  document.title = t('siteTitle');
}

// Init on page load
document.addEventListener('DOMContentLoaded', () => {
  const saved = localStorage.getItem('yishui-lang');
  if (saved && i18n[saved]) {
    setLanguage(saved);
  } else {
    // Detect browser language
    const browserLang = navigator.language.split('-')[0];
    if (i18n[browserLang]) {
      setLanguage(browserLang);
    } else {
      setLanguage('en');
    }
  }
});
